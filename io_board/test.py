class TestIOBoard(unittest.TestCase):

    __brd = IOBoard()

    def test_outputs(self):
        """Test for the relay outputs."""

        for index in range(0, 3):
            self.__brd.set_output(index, True)
            self.assertEqual(True, self.__brd.get_output(index))
            time.sleep(1)

        for index in range(0, 3):
            self.__brd.set_output(index, False)
            self.assertEqual(False, self.__brd.get_output(index))
            time.sleep(1)

    def test_isupper(self):
        self.assertTrue('FOO'.isupper())
        self.assertFalse('Foo'.isupper())

    def test_split(self):
        s = 'hello world'
        self.assertEqual(s.split(), ['hello', 'world'])
        # check that s.split fails when the separator is not a string
        with self.assertRaises(TypeError):
            s.split(2)

